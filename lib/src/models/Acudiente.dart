class Acudiente {
  int idAcudiente;
  String nombre;
  String apellido;
  String direccion;
  String telefono;
  String rolAcudiente;

  Acudiente(int id,String nombre,String apellido,String direccion,String telefono,String rolAcudiente) {
    this.idAcudiente = id;
    this.nombre = nombre;
    this.apellido = apellido;
    this.direccion = direccion;
    this.telefono = telefono;
    this.rolAcudiente = rolAcudiente;
  }
}