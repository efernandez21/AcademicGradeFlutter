class Acudiente {
  int id;
  String nombre;
  String apellido;
  String direccion;
  String telefono;
  String rolAcudiente;

  Acudiente(id, nombre, apellido, direccion, telefono,rolAcudiente) {
    this.id = id;
    this.nombre = nombre;
    this.apellido = apellido;
    this.direccion = direccion;
    this.telefono = telefono;
    this.rolAcudiente = rolAcudiente;
  }
}