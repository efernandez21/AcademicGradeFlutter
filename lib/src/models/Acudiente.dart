class Acudiente {
  int id;
  String nombre;
  String apellido;
  String direccion;
  String telefono;

  Acudiente(id, nombre, apellido, direccion, telefono) {
    this.id = id;
    this.nombre = nombre;
    this.apellido = apellido;
    this.direccion = direccion;
    this.telefono = telefono;
  }
}